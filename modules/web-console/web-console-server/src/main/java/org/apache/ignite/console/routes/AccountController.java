package org.apache.ignite.console.routes;

import org.springframework.context.annotation.Bean;
import org.springframework.context.annotation.Configuration;
import org.springframework.web.reactive.config.EnableWebFlux;
import org.springframework.web.reactive.config.WebFluxConfigurer;
import org.springframework.web.reactive.function.server.RequestPredicates;
import org.springframework.web.reactive.function.server.RouterFunction;
import org.springframework.web.reactive.function.server.RouterFunctions;
import org.springframework.web.reactive.function.server.ServerResponse;

import static org.springframework.http.MediaType.APPLICATION_JSON;
import static org.springframework.web.reactive.function.server.RequestPredicates.POST;
import static org.springframework.web.reactive.function.server.RequestPredicates.path;
import static org.springframework.web.reactive.function.server.RequestPredicates.accept;


@Configuration
public class AccountController implements WebFluxConfigurer {
    @Bean
    public RouterFunction<ServerResponse> route(AccountHandler accountHandler) {
        return RouterFunctions.route()
            .nest(path("/api/v1").and(accept(APPLICATION_JSON)), () ->
                RouterFunctions.route()
//                    .POST("/signin", greetingHandler::hello)
//                    .POST("/signup", greetingHandler::hello)
                    .GET("/user", accountHandler::account)
//                    .POST("/logout", greetingHandler::hello)
                    .build()
            ).build();
    }
}
